package com.Byteschupser.Erntehelfer.ViewModel;

public class Inserat {

    String titel;
    String details;
    String lohn;
    String anzeigeOnlineSeit;

    public String getTitel() {
        return titel;
    }

    public void setTitel(String titel) {
        this.titel = titel;
    }

    public String getDetails() {
        return details;
    }

    public void setDetails(String details) {
        this.details = details;
    }

    public String getLohn() {
        return lohn;
    }

    public void setLohn(String lohn) {
        this.lohn = lohn;
    }

    public String getAnzeigeOnlineSeit() {
        return anzeigeOnlineSeit;
    }

    public void setAnzeigeOnlineSeit(String anzeigeOnlineSeit) {
        this.anzeigeOnlineSeit = anzeigeOnlineSeit;
    }
}
